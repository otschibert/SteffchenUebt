public class DreieckAusgabe {

    public static void main(String[] args) {
        System.out.println();

        for (int zeile = 0; zeile <= 9; zeile++) {
            System.out.println();
            for (int i = 0; i <= zeile; i++) {
                System.out.print(i + " ");
            }
        }


        System.out.println();
    }//psvm
}//pc
